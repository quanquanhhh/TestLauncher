    private void PreviewFolders()
    {
        if (!ValidateRootFolder())
            return;

        List<GroupFolderInfo> folders = CollectGroupFolders(_rootFolder);
        if (folders.Count == 0)
        {
            Debug.LogWarning($"[CheckBundle] 没有在 {_rootFolder} 下找到可用文件夹。");
            return;
        }

        Debug.Log($"[CheckBundle] Preview count = {folders.Count}");
        foreach (var info in folders)
