    private void BuildYooAssetAndOpenLog()
    {
        if (string.IsNullOrWhiteSpace(_packageName))
        {
            EditorUtility.DisplayDialog("CheckBundle", "Package Name 不能为空。", "OK");
            return;
        }

        if (string.IsNullOrWhiteSpace(_buildVersion))
        {
            EditorUtility.DisplayDialog("CheckBundle", "Build Version 不能为空。", "OK");
            return;
        }

        Directory.CreateDirectory(FixedBuildOutputRoot);

        string currentVersionDir = GetCurrentVersionOutputDirectory();
        string compareBuildDir = null;
        Dictionary<string, string> oldHashes;

        // 优先拿“当前版本目录”做对比，并在 build 前改名备份，避免 YooAsset 报目录已存在
        if (Directory.Exists(currentVersionDir))
        {
            oldHashes = SnapshotBundleFileHashes(currentVersionDir, _packageName);

            string backupDir = BackupExistingVersionDirectory(currentVersionDir);
            compareBuildDir = backupDir;

            Debug.Log($"[CheckBundle] Existing version dir moved:\n{currentVersionDir}\n=>\n{backupDir}");
        }
        else
        {
            compareBuildDir = FindLatestBuildVersionDirectory(FixedBuildOutputRoot, _packageName, FixedBuildTarget);
            oldHashes = string.IsNullOrEmpty(compareBuildDir)
                ? new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)
                : SnapshotBundleFileHashes(compareBuildDir, _packageName);
        }

        Debug.Log($"[CheckBundle] Start build. Package={_packageName}, Version={_buildVersion}, Target={FixedBuildTarget}");

        BuildResult buildResult = ExecuteBuild();

        string logPath;
        if (!buildResult.Success)
        {
            logPath = WriteBuildFailLog(compareBuildDir, buildResult);
            OpenWithDefaultApp(logPath);

            Debug.LogError($"[CheckBundle] Build failed : {buildResult.ErrorInfo}");
            EditorUtility.DisplayDialog("CheckBundle", $"Build 失败。\nLog: {logPath}", "OK");
            return;
        }

        string newBuildDir = buildResult.OutputPackageDirectory;
        Dictionary<string, string> newHashes = SnapshotBundleFileHashes(newBuildDir, _packageName);

        UpAssetCopyResult upAssetResult = RefreshUpAssetDirectory(newBuildDir, oldHashes, newHashes);

        logPath = WriteBuildSuccessLog(compareBuildDir, oldHashes, newBuildDir, newHashes, upAssetResult);
        OpenWithDefaultApp(logPath);

        Debug.Log(
            $"[CheckBundle] Build success : {newBuildDir}\n" +
            $"[CheckBundle] upAsset refreshed : {upAssetResult.OutputDir}\n" +
            $"[CheckBundle] copied changed bundles = {upAssetResult.BundleFiles.Count}, metadata files = {upAssetResult.MetadataFiles.Count}");

        EditorUtility.DisplayDialog(
            "CheckBundle",
            $"Build 完成。\n\nupAsset: {upAssetResult.OutputDir}\nChanged Bundles: {upAssetResult.BundleFiles.Count}\nMetadata Files: {upAssetResult.MetadataFiles.Count}\nLog: {logPath}",
            "OK");
    }

    private string GetCurrentVersionOutputDirectory()
    {
        return Path.Combine(
            FixedBuildOutputRoot,
            FixedBuildTarget.ToString(),
            _packageName,
            _buildVersion
        ).Replace("\\", "/");
    }

    private static string BackupExistingVersionDirectory(string versionDir)
    {
        if (string.IsNullOrEmpty(versionDir) || !Directory.Exists(versionDir))
            return versionDir;

        string parentDir = Path.GetDirectoryName(versionDir);
        string folderName = Path.GetFileName(versionDir);

        string time = DateTime.Now.ToString("yyyyMMdd_HHmmss");
        string backupDir = Path.Combine(parentDir, $"{folderName}_old_{time}");

        int index = 1;
        while (Directory.Exists(backupDir))
        {
            backupDir = Path.Combine(parentDir, $"{folderName}_old_{time}_{index}");
            index++;
        }

        Directory.Move(versionDir, backupDir);
        return backupDir.Replace("\\", "/");
    }

    private BuildResult ExecuteBuild()
    {
        ScriptableBuildParameters buildParameters = new ScriptableBuildParameters();
        FillCommonBuildParameters(buildParameters, EBuildPipeline.ScriptableBuildPipeline.ToString());

        // SBP 需要显式设置内置着色器 bundle 名
        buildParameters.BuiltinShadersBundleName = GetBuiltinShaderBundleName(_packageName);

        // 某些版本有这个字段，有些没有，用反射兜底
        SetMemberValue(buildParameters, "EnableSharePackRule", FixedEnableSharePackRule);

        ScriptableBuildPipeline pipeline = new ScriptableBuildPipeline();
        return pipeline.Run(buildParameters, true);
    }

    private void FillCommonBuildParameters(BuildParameters p, string pipelineName)
    {
        p.BuildOutputRoot = FixedBuildOutputRoot;
        p.BuildinFileRoot = AssetBundleBuilderHelper.GetStreamingAssetsRoot();
        p.BuildPipeline = pipelineName;
        p.BuildBundleType = (int)EBuildBundleType.AssetBundle;
        p.BuildTarget = FixedBuildTarget;
        p.PackageName = _packageName;
        p.PackageVersion = _buildVersion;

        SetMemberValue(p, "PackageNote", string.Empty);
        SetMemberValue(p, "VerifyBuildingResult", FixedVerifyBuildingResult);
        SetMemberValue(p, "CompressOption", FixedCompressOption);
        SetMemberValue(p, "FileNameStyle", FixedFileNameStyle);
        SetMemberValue(p, "BuildinFileCopyOption", FixedCopyBuildinFileOption);
        SetMemberValue(p, "BuildinFileCopyParams", FixedCopyBuildinFileParams);
        SetMemberValue(p, "ClearBuildCacheFiles", FixedClearBuildCache);
        SetMemberValue(p, "UseAssetDependencyDB", FixedUseAssetDependDB);

        object encryption = CreateInstanceByTypeName(FixedEncryptionClassName);
        object manifestProcess = CreateInstanceByTypeName(FixedManifestProcessClassName);
        object manifestRestore = CreateInstanceByTypeName(FixedManifestRestoreClassName);

        if (encryption != null) SetMemberValue(p, "EncryptionServices", encryption);
        if (manifestProcess != null) SetMemberValue(p, "ManifestProcessServices", manifestProcess);
        if (manifestRestore != null) SetMemberValue(p, "ManifestRestoreServices", manifestRestore);
    }

    private string GetBuiltinShaderBundleName(string packageName)
    {
        bool uniqueBundleName = false;
        AssetBundleCollectorSetting setting = ResolveCollectorSetting();
        if (setting != null)
            uniqueBundleName = setting.UniqueBundleName;

        var packRuleResult = DefaultPackRule.CreateShadersPackRuleResult();
        return packRuleResult.GetBundleName(packageName, uniqueBundleName);
    }

    private static void SetMemberValue(object obj, string memberName, object value)
    {
        if (obj == null || string.IsNullOrEmpty(memberName))
            return;

        Type type = obj.GetType();

        FieldInfo field = type.GetField(memberName, BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic);
        if (field != null)
        {
            field.SetValue(obj, value);
            return;
        }

        PropertyInfo property = type.GetProperty(memberName, BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic);
        if (property != null && property.CanWrite)
            property.SetValue(obj, value, null);
    }

    private static object CreateInstanceByTypeName(string typeName)
    {
        if (string.IsNullOrWhiteSpace(typeName))
            return null;

        Type type = FindType(typeName);
        if (type == null)
        {
            Debug.LogWarning($"[CheckBundle] Can not find type : {typeName}");
            return null;
        }

        try
        {
            return Activator.CreateInstance(type);
        }
        catch (Exception e)
        {
            Debug.LogWarning($"[CheckBundle] Create instance failed : {typeName}\n{e}");
            return null;
        }
    }

    private static Type FindType(string typeName)
    {
        Type type = Type.GetType(typeName);
        if (type != null)
            return type;

        Assembly[] assemblies = AppDomain.CurrentDomain.GetAssemblies();
        foreach (Assembly asm in assemblies)
        {
            type = asm.GetType(typeName);
            if (type != null)
                return type;
        }

        foreach (Assembly asm in assemblies)
        {
            Type[] types;
            try
            {
                types = asm.GetTypes();
            }
            catch
            {
                continue;
            }

            for (int i = 0; i < types.Length; i++)
            {
                if (types[i].Name == typeName)
                    return types[i];
            }
        }

        return null;
    }

    private static string FindLatestBuildVersionDirectory(string buildOutputRoot, string packageName, BuildTarget buildTarget)
    {
        if (!Directory.Exists(buildOutputRoot))
            return null;

        string[] versionFiles = Directory.GetFiles(buildOutputRoot, $"{packageName}.version", SearchOption.AllDirectories);
        if (versionFiles == null || versionFiles.Length == 0)
            return null;

        string targetMark = "/" + buildTarget + "/";
        string[] targetMatched = versionFiles
            .Select(x => x.Replace("\\", "/"))
            .Where(x => x.IndexOf(targetMark, StringComparison.OrdinalIgnoreCase) >= 0)
            .ToArray();

        string[] finalFiles = targetMatched.Length > 0
            ? targetMatched
            : versionFiles.Select(x => x.Replace("\\", "/")).ToArray();

        string latestDir = finalFiles
            .Select(Path.GetDirectoryName)
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .OrderByDescending(x => Directory.GetLastWriteTimeUtc(x))
            .FirstOrDefault();

        return latestDir;
    }

    private static Dictionary<string, string> SnapshotBundleFileHashes(string versionDir, string packageName)
    {
        Dictionary<string, string> result = new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase);

        if (string.IsNullOrEmpty(versionDir) || !Directory.Exists(versionDir))
            return result;

        string[] files = Directory.GetFiles(versionDir, "*", SearchOption.AllDirectories);
        foreach (string file in files)
        {
            if (ShouldIgnoreDiffFile(file, packageName))
                continue;

            string relative = Path.GetRelativePath(versionDir, file).Replace("\\", "/");
            result[relative] = CalcMD5(file);
        }

        return result;
    }

    private static bool ShouldIgnoreDiffFile(string filePath, string packageName)
    {
        string fileName = Path.GetFileName(filePath);
        string ext = Path.GetExtension(fileName).ToLowerInvariant();

        if (fileName.Equals($"{packageName}.version", StringComparison.OrdinalIgnoreCase))
            return true;

        switch (ext)
        {
            case ".hash":
            case ".json":
            case ".bytes":
            case ".report":
            case ".manifest":
            case ".meta":
            case ".txt":
            case ".log":
                return true;
        }

        return false;
    }

    private static string CalcMD5(string filePath)
    {
        using (var stream = File.OpenRead(filePath))
        using (var md5 = MD5.Create())
        {
            byte[] hash = md5.ComputeHash(stream);
            StringBuilder sb = new StringBuilder(hash.Length * 2);
            for (int i = 0; i < hash.Length; i++)
                sb.Append(hash[i].ToString("x2"));
            return sb.ToString();
        }
    }

