    private bool SyncToYooAssetInternal(bool showDialog)
    {
        if (!ValidateRootFolder())
            return false;

        AssetBundleCollectorSetting setting = ResolveCollectorSetting();
        if (setting == null)
        {
            if (showDialog)
                EditorUtility.DisplayDialog("CheckBundle", "没有找到 AssetBundleCollectorSetting 资源。请先创建 YooAsset 的 Collector Setting。", "OK");
            return false;
        }

        Undo.RecordObject(setting, "Tool/CheckBundle Sync");

        AssetBundleCollectorPackage package = GetOrCreatePackage(setting, _packageName);
        if (package.Groups == null)
            package.Groups = new List<AssetBundleCollectorGroup>();

        List<GroupFolderInfo> folderInfos = CollectGroupFolders(_rootFolder);
        HashSet<string> currentAutoGroupNames = new HashSet<string>(folderInfos.Select(x => x.GroupName), StringComparer.OrdinalIgnoreCase);

        int created = 0;
        int updated = 0;
        int removed = 0;

        foreach (var info in folderInfos)
        {
            bool isNew;
            AssetBundleCollectorGroup group = GetOrCreateGroup(package, info.GroupName, out isNew);
            ConfigureGroup(group, info, _clearManagedCollectors);

            if (isNew)
                created++;
            else
                updated++;
        }

        if (_removeStaleManagedGroups)
        {
            for (int i = package.Groups.Count - 1; i >= 0; i--)
            {
                AssetBundleCollectorGroup group = package.Groups[i];
                if (group == null)
                    continue;

                if (!currentAutoGroupNames.Contains(group.GroupName))
                {
                    package.Groups.RemoveAt(i);
                    removed++;
                }
            }
        }

        package.FixConfigError();
        setting.FixAllPackageConfigError();

        EditorUtility.SetDirty(setting);
        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();

        Debug.Log(
            $"[CheckBundle] Sync finished. " +
            $"Package={_packageName}, Root={_rootFolder}, " +
            $"Created={created}, Updated={updated}, Removed={removed}, TotalFolders={folderInfos.Count}");

        if (showDialog)
        {
            EditorUtility.DisplayDialog(
                "CheckBundle",
                $"同步完成。\n\nCreated: {created}\nUpdated: {updated}\nRemoved: {removed}\nFolders: {folderInfos.Count}",
                "OK");
        }

        return true;
    }

    private void ProcessAtlases()
    {
        if (!ValidateRootFolder())
            return;

        List<GroupFolderInfo> folderInfos = CollectGroupFolders(_rootFolder);
        if (folderInfos.Count == 0)
        {
            EditorUtility.DisplayDialog("CheckBundle", "没有找到可处理的文件夹。", "OK");
            return;
        }

        string platformName = GetAtlasPlatformName(FixedBuildTarget);

        int processedFolderCount = 0;
        int skippedFolderCount = 0;
        int changedTextureCount = 0;
        int createdAtlasCount = 0;
        int updatedAtlasCount = 0;
        int failedCount = 0;

        try
        {
            for (int i = 0; i < folderInfos.Count; i++)
            {
                GroupFolderInfo info = folderInfos[i];
                string atlasName = GetAtlasName(info.GroupName);

                EditorUtility.DisplayProgressBar(
                    "处理图集",
                    $"{atlasName} ({i + 1}/{folderInfos.Count})",
                    (i + 1f) / folderInfos.Count);

                if (!AssetDatabase.IsValidFolder(info.AssetFolderPath))
                {
                    skippedFolderCount++;
                    continue;
                }

                // 如果当前文件夹里已经有同名图集，就直接跳过，避免重复处理导致 hash 变化
                if (HasAtlasInFolder(info.AssetFolderPath, atlasName))
                {
                    skippedFolderCount++;
                    Debug.Log($"[CheckBundle] Skip atlas process, already exists: {info.AssetFolderPath}/{atlasName}.spriteatlas");
                    continue;
                }

                List<string> texturePaths = GetTexturePathsInFolder(info.AssetFolderPath);
                if (texturePaths.Count == 0)
                {
                    skippedFolderCount++;
                    continue;
                }

                processedFolderCount++;

                changedTextureCount += PrepareTexturesForAtlas(texturePaths);

                bool atlasCreated;
                SpriteAtlas atlas = FindOrCreateAtlas(atlasName, info.AssetFolderPath, out atlasCreated);
                if (atlas == null)
                {
                    failedCount++;
                    Debug.LogError($"[CheckBundle] 图集创建失败：{atlasName}  Folder={info.AssetFolderPath}");
                    continue;
                }

                if (atlasCreated)
                    createdAtlasCount++;
                else
                    updatedAtlasCount++;

                EnsureFolderPackedInAtlas(atlas, info.AssetFolderPath);
                ApplyAtlasSettings(atlas, platformName);
                EditorUtility.SetDirty(atlas);

                Debug.Log($"[CheckBundle] 处理图集完成：{atlasName}  Folder={info.AssetFolderPath}");
            }
        }
        finally
        {
            EditorUtility.ClearProgressBar();
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();
        }

        string message =
            $"处理完成。\n\n" +
            $"Processed Folders: {processedFolderCount}\n" +
            $"Skipped Folders: {skippedFolderCount}\n" +
            $"Changed Textures: {changedTextureCount}\n" +
            $"Created Atlases: {createdAtlasCount}\n" +
            $"Updated Atlases: {updatedAtlasCount}\n" +
            $"Failed: {failedCount}";

        Debug.Log($"[CheckBundle] Atlas Process Finished.\n{message}");
        EditorUtility.DisplayDialog("CheckBundle", message, "OK");
    }

    private bool ValidateRootFolder()
    {
        if (string.IsNullOrWhiteSpace(_rootFolder))
        {
            EditorUtility.DisplayDialog("CheckBundle", "Root Folder 不能为空。", "OK");
            return false;
        }

        if (!AssetDatabase.IsValidFolder(_rootFolder))
        {
            EditorUtility.DisplayDialog("CheckBundle", $"目录不存在：{_rootFolder}", "OK");
            return false;
        }

        return true;
    }

    private static AssetBundleCollectorSetting ResolveCollectorSetting()
    {
        string[] guids = AssetDatabase.FindAssets("t:AssetBundleCollectorSetting");
        if (guids == null || guids.Length == 0)
            return null;

        if (guids.Length > 1)
            Debug.LogWarning("[CheckBundle] Found multiple AssetBundleCollectorSetting assets. 将使用第一个。");

        string path = AssetDatabase.GUIDToAssetPath(guids[0]);
        return AssetDatabase.LoadAssetAtPath<AssetBundleCollectorSetting>(path);
    }

    private static AssetBundleCollectorPackage GetOrCreatePackage(AssetBundleCollectorSetting setting, string packageName)
    {
        if (setting.Packages == null)
            setting.Packages = new List<AssetBundleCollectorPackage>();

        AssetBundleCollectorPackage package = setting.Packages.FirstOrDefault(x => x != null && x.PackageName == packageName);
        if (package != null)
            return package;

        package = new AssetBundleCollectorPackage
        {
            PackageName = packageName,
            PackageDesc = "Auto created by Tool/CheckBundle",
            EnableAddressable = true,
            SupportExtensionless = false,
            LocationToLower = false,
            IncludeAssetGUID = false,
            AutoCollectShaders = false,
            IgnoreRuleName = nameof(NormalIgnoreRule),
            Groups = new List<AssetBundleCollectorGroup>()
        };

        setting.Packages.Add(package);
        return package;
    }

    private static AssetBundleCollectorGroup GetOrCreateGroup(
        AssetBundleCollectorPackage package,
        string groupName,
        out bool isNew)
    {
        AssetBundleCollectorGroup found = null;

        if (package.Groups != null)
            found = package.Groups.FirstOrDefault(x => x != null && x.GroupName == groupName);

        if (found != null)
        {
            isNew = false;
            return found;
        }

        var group = new AssetBundleCollectorGroup
        {
            GroupName = groupName,
            // GroupDesc = AutoDescPrefix,
            AssetTags = groupName,
            ActiveRuleName = nameof(EnableGroup),
            Collectors = new List<AssetBundleCollector>()
        };

        package.Groups.Add(group);
        isNew = true;
        return group;
    }

    private static void ConfigureGroup(
        AssetBundleCollectorGroup group,
        GroupFolderInfo info,
        bool clearManagedCollectors)
    {
        group.GroupName = info.GroupName;
        // group.GroupDesc = $"{AutoDescPrefix}|{info.AssetFolderPath}";
        group.AssetTags = info.AssetTag;
        group.ActiveRuleName = nameof(EnableGroup);

        if (group.Collectors == null)
            group.Collectors = new List<AssetBundleCollector>();

        AssetBundleCollector collector = null;

        if (!clearManagedCollectors)
            collector = group.Collectors.FirstOrDefault(x => x != null && x.CollectPath == info.AssetFolderPath);
        else
            group.Collectors.Clear();

        if (collector == null)
        {
            collector = new AssetBundleCollector();
            group.Collectors.Add(collector);
        }

        ConfigureCollector(collector, info);
    }

    private static void ConfigureCollector(AssetBundleCollector collector, GroupFolderInfo info)
    {
        collector.CollectPath = info.AssetFolderPath;
        collector.CollectorGUID = AssetDatabase.AssetPathToGUID(info.AssetFolderPath);
        collector.CollectorType = ECollectorType.MainAssetCollector;
        collector.AddressRuleName = nameof(AddressByFileName);
        collector.PackRuleName = nameof(PackGroup);
        collector.FilterRuleName = nameof(CollectAll);
        collector.AssetTags = info.AssetTag;
        collector.UserData = string.Empty;
    }

