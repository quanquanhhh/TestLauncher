    private static List<GroupFolderInfo> CollectGroupFolders(string rootFolder)
    {
        List<GroupFolderInfo> results = new List<GroupFolderInfo>();
        string rootFullPath = ToFullPath(rootFolder);
        if (!Directory.Exists(rootFullPath))
            return results;

        string[] allDirs = Directory.GetDirectories(rootFullPath, "*", SearchOption.AllDirectories);
        foreach (string dir in allDirs)
        {
            string assetPath = ToAssetPath(dir);
            if (string.IsNullOrEmpty(assetPath))
                continue;

            if (!AssetDatabase.IsValidFolder(assetPath))
                continue;

            if (ShouldSkipFolder(rootFolder, assetPath))
                continue;

            if (!ContainsDirectAssetFiles(dir))
                continue;

            string groupName = BuildGroupName(rootFolder, assetPath);
            if (string.IsNullOrEmpty(groupName))
                continue;

            results.Add(new GroupFolderInfo
            {
                AssetFolderPath = assetPath,
                GroupName = groupName,
                AssetTag = groupName
            });
        }

        results.Sort((a, b) => string.CompareOrdinal(a.AssetFolderPath, b.AssetFolderPath));
        return results;
    }

    private static bool ShouldSkipFolder(string rootFolder, string assetPath)
    {
        string relative = MakeRelativeAssetPath(rootFolder, assetPath);
        if (string.IsNullOrEmpty(relative))
            return true;

        string[] parts = relative.Split(new[] { '/' }, StringSplitOptions.RemoveEmptyEntries);
        if (parts.Length == 0)
            return true;

        if (string.Equals(parts[0], "BuildIn", StringComparison.OrdinalIgnoreCase))
            return true;

        return false;
    }

    private static bool ContainsDirectAssetFiles(string fullDir)
    {
        string[] files = Directory.GetFiles(fullDir, "*", SearchOption.TopDirectoryOnly);
        foreach (string file in files)
        {
            string ext = Path.GetExtension(file);
            if (string.Equals(ext, ".meta", StringComparison.OrdinalIgnoreCase))
                continue;

            string fileName = Path.GetFileName(file);
            if (string.Equals(fileName, ".DS_Store", StringComparison.OrdinalIgnoreCase))
                continue;

            return true;
        }

        return false;
    }

    private static string BuildGroupName(string rootFolder, string folderAssetPath)
    {
        string relative = MakeRelativeAssetPath(rootFolder, folderAssetPath);
        if (string.IsNullOrEmpty(relative))
            return string.Empty;

        string[] parts = relative.Split(new[] { '/' }, StringSplitOptions.RemoveEmptyEntries);
        if (parts.Length == 0)
            return string.Empty;

        for (int i = 0; i < parts.Length; i++)
            parts[i] = SanitizeName(parts[i]);

        return string.Join("_", parts);
    }

    private static string SanitizeName(string value)
    {
        if (string.IsNullOrEmpty(value))
            return string.Empty;

        value = value.Replace(" ", "_");
        value = value.Replace(";", "_");
        return value.Trim();
    }

    private static string MakeRelativeAssetPath(string rootFolder, string targetFolder)
    {
        string root = rootFolder.Replace("\\", "/").TrimEnd('/');
        string target = targetFolder.Replace("\\", "/").TrimEnd('/');

        if (!target.StartsWith(root + "/", StringComparison.OrdinalIgnoreCase))
            return string.Empty;

        return target.Substring(root.Length + 1);
    }

    private static string ToFullPath(string assetPath)
    {
        string projectRoot = Directory.GetParent(Application.dataPath).FullName.Replace("\\", "/");
        string path = assetPath.Replace("\\", "/");
        return Path.GetFullPath(Path.Combine(projectRoot, path)).Replace("\\", "/");
    }

    private static string ToAssetPath(string fullPath)
    {
        string normalized = fullPath.Replace("\\", "/");
        string projectRoot = Directory.GetParent(Application.dataPath).FullName.Replace("\\", "/");

        if (!normalized.StartsWith(projectRoot, StringComparison.OrdinalIgnoreCase))
            return null;

        string relative = normalized.Substring(projectRoot.Length).TrimStart('/');
        return relative;
    }

