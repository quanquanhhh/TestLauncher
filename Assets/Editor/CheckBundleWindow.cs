    private static string GetAtlasName(string groupName)
    {
        if (string.IsNullOrWhiteSpace(groupName))
            return string.Empty;

        if (groupName.EndsWith("_asset", StringComparison.OrdinalIgnoreCase))
            return groupName;

        return groupName + "_asset";
    }

    private static bool HasAtlasInFolder(string folderAssetPath, string atlasName)
    {
        if (string.IsNullOrEmpty(folderAssetPath) || string.IsNullOrEmpty(atlasName))
            return false;

        string atlasPath = $"{folderAssetPath}/{atlasName}.spriteatlas";
        SpriteAtlas atlas = AssetDatabase.LoadAssetAtPath<SpriteAtlas>(atlasPath);
        return atlas != null;
    }

    private static List<string> GetTexturePathsInFolder(string folderAssetPath)
    {
        List<string> result = new List<string>();

        string[] guids = AssetDatabase.FindAssets("t:Texture2D", new[] { folderAssetPath });
        foreach (string guid in guids)
        {
            string path = AssetDatabase.GUIDToAssetPath(guid);
            if (string.IsNullOrEmpty(path))
                continue;

            if (!IsTextureFile(path))
                continue;

            result.Add(path);
        }

        result.Sort(StringComparer.OrdinalIgnoreCase);
        return result;
    }

    private static bool IsTextureFile(string assetPath)
    {
        string ext = Path.GetExtension(assetPath).ToLowerInvariant();
        switch (ext)
        {
            case ".png":
            case ".jpg":
            case ".jpeg":
            case ".tga":
            case ".psd":
            case ".tif":
            case ".tiff":
            case ".bmp":
            case ".gif":
                return true;
            default:
                return false;
        }
    }

    private static int PrepareTexturesForAtlas(List<string> texturePaths)
    {
        int changedCount = 0;

        foreach (string texturePath in texturePaths)
        {
            TextureImporter importer = AssetImporter.GetAtPath(texturePath) as TextureImporter;
            if (importer == null)
                continue;

            bool changed = false;

            if (importer.textureType != TextureImporterType.Sprite)
            {
                importer.textureType = TextureImporterType.Sprite;
                changed = true;
            }

            if (importer.mipmapEnabled)
            {
                importer.mipmapEnabled = false;
                changed = true;
            }

            if (changed)
            {
                importer.SaveAndReimport();
                changedCount++;
            }
        }

        return changedCount;
    }

    private static SpriteAtlas FindOrCreateAtlas(string atlasName, string folderAssetPath, out bool created)
    {
        created = false;

        string atlasPathInFolder = $"{folderAssetPath}/{atlasName}.spriteatlas";
        SpriteAtlas atlas = AssetDatabase.LoadAssetAtPath<SpriteAtlas>(atlasPathInFolder);
        if (atlas != null)
            return atlas;

        string[] guids = AssetDatabase.FindAssets($"{atlasName} t:SpriteAtlas");
        List<string> matchedPaths = new List<string>();

        foreach (string guid in guids)
        {
            string path = AssetDatabase.GUIDToAssetPath(guid);
            if (string.Equals(Path.GetFileNameWithoutExtension(path), atlasName, StringComparison.OrdinalIgnoreCase))
                matchedPaths.Add(path);
        }

        if (matchedPaths.Count > 0)
        {
            string preferred = matchedPaths.FirstOrDefault(x =>
                string.Equals(
                    NormalizePath(Path.GetDirectoryName(x)),
                    NormalizePath(folderAssetPath),
                    StringComparison.OrdinalIgnoreCase));

            string chosenPath = preferred ?? matchedPaths[0];
            atlas = AssetDatabase.LoadAssetAtPath<SpriteAtlas>(chosenPath);

            if (matchedPaths.Count > 1)
            {
                Debug.LogWarning(
                    $"[CheckBundle] 找到多个同名图集 {atlasName}，本次使用：{chosenPath}\n" +
                    string.Join("\n", matchedPaths));
            }

            return atlas;
        }

        atlas = new SpriteAtlas();
        AssetDatabase.CreateAsset(atlas, atlasPathInFolder);
        created = true;

        Debug.Log($"[CheckBundle] 已创建图集：{atlasPathInFolder}");
        return atlas;
    }

    private static void EnsureFolderPackedInAtlas(SpriteAtlas atlas, string folderAssetPath)
    {
        DefaultAsset folderObj = AssetDatabase.LoadAssetAtPath<DefaultAsset>(folderAssetPath);
        if (folderObj == null)
        {
            Debug.LogError($"[CheckBundle] 无法加载文件夹对象：{folderAssetPath}");
            return;
        }

        UnityEngine.Object[] packables = atlas.GetPackables();
        bool alreadyHasFolder = packables.Any(x =>
            string.Equals(
                NormalizePath(AssetDatabase.GetAssetPath(x)),
                NormalizePath(folderAssetPath),
                StringComparison.OrdinalIgnoreCase));

        if (alreadyHasFolder)
            return;

        List<UnityEngine.Object> removable = new List<UnityEngine.Object>();
        foreach (UnityEngine.Object obj in packables)
        {
            if (obj == null)
                continue;

            string packablePath = NormalizePath(AssetDatabase.GetAssetPath(obj));
            if (string.IsNullOrEmpty(packablePath))
                continue;

            if (string.Equals(packablePath, NormalizePath(folderAssetPath), StringComparison.OrdinalIgnoreCase))
                continue;

            if (IsPathInFolder(packablePath, folderAssetPath))
                removable.Add(obj);
        }

        if (removable.Count > 0)
            atlas.Remove(removable.ToArray());

        atlas.Add(new UnityEngine.Object[] { folderObj });
    }

    private static void ApplyAtlasSettings(SpriteAtlas atlas, string platformName)
    {
        SpriteAtlasPackingSettings packingSettings = atlas.GetPackingSettings();
        packingSettings.enableRotation = false;
        packingSettings.enableTightPacking = false;
        atlas.SetPackingSettings(packingSettings);

        SpriteAtlasTextureSettings textureSettings = atlas.GetTextureSettings();
        textureSettings.generateMipMaps = false;
        atlas.SetTextureSettings(textureSettings);

        if (!string.IsNullOrEmpty(platformName))
        {
            TextureImporterPlatformSettings platformSettings = atlas.GetPlatformSettings(platformName);
            platformSettings.overridden = true;
            platformSettings.format = TextureImporterFormat.ASTC_6x6;
            platformSettings.crunchedCompression = false;
            platformSettings.compressionQuality = 100;
            atlas.SetPlatformSettings(platformSettings);
        }
    }

