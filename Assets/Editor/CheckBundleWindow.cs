    private string WriteBuildSuccessLog(
        string oldBuildDir,
        Dictionary<string, string> oldHashes,
        string newBuildDir,
        Dictionary<string, string> newHashes,
        UpAssetCopyResult upAssetResult)
    {
        string logDir = GetLogDirectory();
        string logPath = Path.Combine(logDir, $"{_packageName}_{_buildVersion}_{DateTime.Now:yyyyMMdd_HHmmss}.txt");

        HashSet<string> allKeys = new HashSet<string>(oldHashes.Keys, StringComparer.OrdinalIgnoreCase);
        allKeys.UnionWith(newHashes.Keys);

        List<string> changed = new List<string>();
        List<string> added = new List<string>();
        List<string> removed = new List<string>();

        foreach (string key in allKeys.OrderBy(x => x, StringComparer.OrdinalIgnoreCase))
        {
            bool hasOld = oldHashes.TryGetValue(key, out string oldHash);
            bool hasNew = newHashes.TryGetValue(key, out string newHash);

            string displayName = GetDisplayBundleName(key);

            if (hasOld && hasNew)
            {
                if (!string.Equals(oldHash, newHash, StringComparison.OrdinalIgnoreCase))
                    changed.Add($"[{displayName}]  {oldHash}  --------->  {newHash};");
            }
            else if (!hasOld && hasNew)
            {
                added.Add($"[{displayName}]  <new>  --------->  {newHash};");
            }
            else if (hasOld && !hasNew)
            {
                removed.Add($"[{displayName}]  {oldHash}  --------->  <removed>;");
            }
        }

        StringBuilder sb = new StringBuilder();
        sb.AppendLine("========== YooAsset Build Log ==========");
        sb.AppendLine($"Time                     : {DateTime.Now:yyyy-MM-dd HH:mm:ss}");
        sb.AppendLine($"Package                  : {_packageName}");
        sb.AppendLine($"BuildVersion             : {_buildVersion}");
        sb.AppendLine($"BuildTarget              : {FixedBuildTarget}");
        sb.AppendLine($"BuildPipeline            : {EBuildPipeline.ScriptableBuildPipeline}");
        sb.AppendLine($"BuildOutputRoot          : {FixedBuildOutputRoot}");
        sb.AppendLine($"PreviousBuild            : {oldBuildDir}");
        sb.AppendLine($"CurrentBuild             : {newBuildDir}");
        sb.AppendLine($"UpAssetDirectory         : {upAssetResult.OutputDir}");
        sb.AppendLine($"ClearBuildCache          : {FixedClearBuildCache}");
        sb.AppendLine($"UseAssetDependDB         : {FixedUseAssetDependDB}");
        sb.AppendLine($"Compression              : {FixedCompressOption}");
        sb.AppendLine($"FileNameStyle            : {FixedFileNameStyle}");
        sb.AppendLine($"CopyBuildinFileOption    : {FixedCopyBuildinFileOption}");
        sb.AppendLine($"CopyBuildinFileParams    : {FixedCopyBuildinFileParams}");
        sb.AppendLine($"EncryptionServices       : {FixedEncryptionClassName}");
        sb.AppendLine($"ManifestProcessServices  : {FixedManifestProcessClassName}");
        sb.AppendLine($"ManifestRestoreServices  : {FixedManifestRestoreClassName}");
        sb.AppendLine($"Result                   : SUCCESS");
        sb.AppendLine();

        sb.AppendLine("---------- Summary ----------");
        sb.AppendLine($"Changed Bundles          : {changed.Count}");
        sb.AppendLine($"Added Bundles            : {added.Count}");
        sb.AppendLine($"Removed Bundles          : {removed.Count}");
        sb.AppendLine($"Copied To upAsset        : {upAssetResult.BundleFiles.Count}");
        sb.AppendLine($"Copied Meta To upAsset   : {upAssetResult.MetadataFiles.Count}");
        sb.AppendLine();

        sb.AppendLine("---------- Changed ----------");
        if (changed.Count == 0) sb.AppendLine("(none)");
        foreach (string line in changed) sb.AppendLine(line);
        sb.AppendLine();

        sb.AppendLine("---------- Added ----------");
        if (added.Count == 0) sb.AppendLine("(none)");
        foreach (string line in added) sb.AppendLine(line);
        sb.AppendLine();

        sb.AppendLine("---------- Removed ----------");
        if (removed.Count == 0) sb.AppendLine("(none)");
        foreach (string line in removed) sb.AppendLine(line);
        sb.AppendLine();

        sb.AppendLine("---------- upAsset Bundles ----------");
        if (upAssetResult.BundleFiles.Count == 0) sb.AppendLine("(none)");
        foreach (string line in upAssetResult.BundleFiles.OrderBy(x => x, StringComparer.OrdinalIgnoreCase))
            sb.AppendLine(line);
        sb.AppendLine();

        sb.AppendLine("---------- upAsset Metadata ----------");
        if (upAssetResult.MetadataFiles.Count == 0) sb.AppendLine("(none)");
        foreach (string line in upAssetResult.MetadataFiles.OrderBy(x => x, StringComparer.OrdinalIgnoreCase))
            sb.AppendLine(line);

        File.WriteAllText(logPath, sb.ToString(), Encoding.UTF8);
        return logPath;
    }

    private UpAssetCopyResult RefreshUpAssetDirectory(
        string newBuildDir,
        Dictionary<string, string> oldHashes,
        Dictionary<string, string> newHashes)
    {
        UpAssetCopyResult result = new UpAssetCopyResult
        {
            OutputDir = GetUpAssetOutputDirectory(newBuildDir)
        };

        ResetDirectory(result.OutputDir);

        HashSet<string> copiedFiles = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        // 1. 复制变化 / 新增的 bundle
        foreach (var pair in newHashes.OrderBy(x => x.Key, StringComparer.OrdinalIgnoreCase))
        {
            string relativePath = pair.Key;
            bool hasOld = oldHashes.TryGetValue(relativePath, out string oldHash);
            bool isChangedOrAdded = !hasOld || !string.Equals(oldHash, pair.Value, StringComparison.OrdinalIgnoreCase);

            if (!isChangedOrAdded)
                continue;

            CopyRelativeFile(newBuildDir, relativePath, result.OutputDir);
            result.BundleFiles.Add(relativePath);
            copiedFiles.Add(relativePath);
        }

        // 2. 始终复制版本/清单相关文件，保证传到 S3 后可直接用于热更
        string[] allFiles = Directory.GetFiles(newBuildDir, "*", SearchOption.AllDirectories);
        foreach (string file in allFiles.OrderBy(x => x, StringComparer.OrdinalIgnoreCase))
        {
            string relativePath = Path.GetRelativePath(newBuildDir, file).Replace("\\", "/");
            if (!ShouldAlwaysCopyToUpAsset(relativePath, _packageName))
                continue;

            if (copiedFiles.Contains(relativePath))
                continue;

            CopyRelativeFile(newBuildDir, relativePath, result.OutputDir);
            result.MetadataFiles.Add(relativePath);
            copiedFiles.Add(relativePath);
        }

        return result;
    }

    private static string GetUpAssetOutputDirectory(string buildDir)
    {
        if (string.IsNullOrEmpty(buildDir))
            return string.Empty;

        string packageDir = Path.GetDirectoryName(buildDir);
        return Path.Combine(packageDir, "upAsset").Replace("\\", "/");
    }

    private static void ResetDirectory(string dir)
    {
        if (string.IsNullOrEmpty(dir))
            return;

        if (Directory.Exists(dir))
            Directory.Delete(dir, true);

        Directory.CreateDirectory(dir);
    }

    private static void CopyRelativeFile(string sourceRoot, string relativePath, string targetRoot)
    {
        string sourcePath = Path.Combine(sourceRoot, relativePath);
        string targetPath = Path.Combine(targetRoot, relativePath);

        string targetDir = Path.GetDirectoryName(targetPath);
        if (!string.IsNullOrEmpty(targetDir))
            Directory.CreateDirectory(targetDir);

        File.Copy(sourcePath, targetPath, true);
    }

    private static bool ShouldAlwaysCopyToUpAsset(string relativePath, string packageName)
    {
        if (string.IsNullOrEmpty(relativePath))
            return false;

        string fileName = Path.GetFileName(relativePath);
        string ext = Path.GetExtension(fileName).ToLowerInvariant();

        if (fileName.Equals($"{packageName}.version", StringComparison.OrdinalIgnoreCase))
            return true;

        switch (ext)
        {
            case ".hash":
            case ".json":
            case ".bytes":
            case ".manifest":
                return true;
            default:
                return false;
        }
    }

    private static string GetDisplayBundleName(string relativePath)
    {
        if (string.IsNullOrEmpty(relativePath))
            return string.Empty;

        string fileName = Path.GetFileName(relativePath);
        string ext = Path.GetExtension(fileName);
        if (!string.IsNullOrEmpty(ext))
            return Path.GetFileNameWithoutExtension(fileName);

        return fileName;
    }

    private static void OpenWithDefaultApp(string path)
    {
        if (string.IsNullOrEmpty(path) || !File.Exists(path))
            return;

        try
        {
            Process.Start(new ProcessStartInfo
            {
                FileName = path,
                UseShellExecute = true
            });
        }
        catch (Exception e)
        {
            Debug.LogWarning($"[CheckBundle] Open log failed : {e.Message}\nPath={path}");
            EditorUtility.RevealInFinder(path);
        }
    }

    private static string GetLogDirectory()
    {
        string projectRoot = Directory.GetParent(Application.dataPath).FullName;
        string logDir = Path.Combine(projectRoot, "Library", "CheckBundleLogs");
        Directory.CreateDirectory(logDir);
        return logDir;
    }

